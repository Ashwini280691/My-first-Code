print("Hi, Enter your marks: ")
marks = int(input())
if marks >= 90:
    print("You got an A grade.")
    print("Congratulations!You are a rockstar!")
elif marks >= 80:
    print("You got a B grade.")
elif marks >= 70:
    print("You got a C grade.")
elif marks >= 60:
    print("You got a D grade.")
else:
    print("You got an F grade.")
